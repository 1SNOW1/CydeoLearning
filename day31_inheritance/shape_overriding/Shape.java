package day31_inheritance.shape_overriding;

public class Shape {

    private double circle, square, rectangle, triangle;


    public double getCircle() {
        return circle;
    }

    public void setCircle(double circle) {
        this.circle = circle;
    }

    public double getSquare() {
        return square;
    }

    public void setSquare(double square) {
        this.square = square;
    }

    public double getRectangle() {
        return rectangle;
    }

    public void setRectangle(double rectangle) {
        this.rectangle = rectangle;
    }

    public double getTriangle() {
        return triangle;
    }

    public void setTriangle(double triangle) {
        this.triangle = triangle;
    }
}

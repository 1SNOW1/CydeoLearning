package day16_nestedLoop;

public class nestedLoop1 {

    public static void main(String[] args) {

        /*
        This loop below is basically, whenever the outer loop runs one time the nested loop will run 5 times.
        At the end we will see 25 Wooden Spoons.
         */
        for (int i = 0; i < 5 ; i++) {

            for (int j = 0; j < 5; j++) {
                System.out.println("Wooden Spoon");

            }




        }
    }
}

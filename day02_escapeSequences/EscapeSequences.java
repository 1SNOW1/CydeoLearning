package day02_escapeSequences;

public class EscapeSequences {

    public static void main(String[] args) {

        System.out.println("Hello students\nToday we have a Java class");


        System.out.println("------------------------------------------------------");


        System.out.println("\tJava programming language");
        System.out.println("\t\tJava developer course batch 1");

        System.out.println("------------------------------------------------------");

        System.out.println("\\");

        System.out.println("-------------------------------------------------------");

        System.out.println("My favorite tv show is \"The walking dead\"");



        /*
                1. \n : New line
                2. \t : paragraph space
                3. \\ : back slash "\"
                4. \" : double quotes "
         */
    }
}

package day5_operators;

public class unaryOperators {

    public static void main(String[] args) {

        //pre increment/decrement

        int x = 10;

        System.out.println(++x);
        System.out.println(x);
        System.out.println(--x);


        System.out.println("------------------------------------------------------------------");

        //post increment/decrement

        int y = 10;

        System.out.println(y++);
        System.out.println(y);
        System.out.println(y--);
        System.out.println(y);
    }
}

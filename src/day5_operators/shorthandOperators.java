package day5_operators;

public class shorthandOperators {

    public static void main(String[] args) {

        double salary = 10; 
        
        salary *= 10;

        System.out.println("salary = " + salary);
        
        salary /= 3;

        System.out.println("salary = " + salary);
        
        salary %= 33.33333333333333;

        System.out.println("salary = " + salary);
    }
}

package day16_nestedLoop;

public class doWhileLoop {

    public static void main(String[] args) {

        boolean condition = false;

        for (;condition;) {
            System.out.println("Hello cydeo--------------for loop");
        }

        while(condition){
            System.out.println("Hello cydeo brothers ----------- while loop");
        }

        do {
            System.out.println("Hello cydeo brothers----------------do while loop");

        }while (condition);
    }
}
